package ua.lgs;

public class VarHometask {
	public static void main(String[] args) {
		// Оголошуємо примітивні типи у змінних
		int intVar = 7;
		System.out.println("My integer number = " + intVar);

		short shoVar = 2;
		System.out.println("My short  number = " + shoVar);

		long lonVar = 115L;
		System.out.println("My long number = " + lonVar);

		byte byVar = 7;
		System.out.println("My byte number = " + byVar);
		
		float floatNumber = 8.11f;
		System.out.println("My float number = " + floatNumber);
		
		char textvar = 'I';
		System.out.println("My char variable = " + textvar);
		
		boolean boolVar = true;
		System.out.println("My boolean variable = " + boolVar);
		
		double doubVar = 6.18;
		System.out.println("My double variable = " + doubVar);

	}
}
